package edu.msg.flightmanager.backend.dto;

import java.util.Date;

public class FlightTemplateDto extends EntityDto {

	private AirportDto departure;
	private AirportDto arrival;
	private ItineraryDto itinerary;
	private String code;
	private Date availableFrom;
	private CompanyDto company;

	public CompanyDto getCompany() {
		return company;
	}

	public void setCompany(CompanyDto company) {
		this.company = company;
	}

	public Date getAvailableFrom() {
		return availableFrom;
	}

	public void setAvailableFrom(Date availableFrom) {
		this.availableFrom = availableFrom;
	}

	public AirportDto getDeparture() {
		return departure;
	}

	public void setDeparture(AirportDto departure) {
		this.departure = departure;
	}

	public AirportDto getArrival() {
		return arrival;
	}

	public void setArrival(AirportDto arrival) {
		this.arrival = arrival;
	}

	public ItineraryDto getItinerary() {
		return itinerary;
	}

	public void setItinerary(ItineraryDto itinerary) {
		this.itinerary = itinerary;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
